    private void getAllEmp()
 {
       List<empModel> empListResult= fonctionRepository.getAllEmp();
       DefaultTableModel model = (DefaultTableModel) EmpTable.getModel();
       model.setRowCount(0);

       for(int i=0;i<empListResult.size();i++)
       {
           var emp = empListResult.get(i);
           
     String  NomFunction="";
     
            for(int j=0;j<fonctions.size();j++)
            {
                    if(fonctions.get(j).id_fonction==emp.id_fonction)
                    {
                        NomFunction = fonctions.get(j).nom_fonction;
                        break;
                    }
                    
            }          
           model.addRow(new Object[]{emp.Cin_emp,emp.nom_emp,emp.prenom_emp,NomFunction,emp.nom_service});              
       }

 }



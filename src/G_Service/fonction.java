    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        int idFunction=0;
        for(int i=0;i<fonctions.size();i++)
        {
            if(fonctions.get(i).nom_fonction==functionsComboBox.getSelectedItem().toString())
            {
                idFunction = fonctions.get(i).id_fonction;

                break;
            }
        }

        DefaultTableModel model= (DefaultTableModel)EmpTable.getModel();
        int selectedRowIndex= EmpTable.getSelectedRow();
        String  cinEmp =(model.getValueAt(selectedRowIndex,0).toString());
        // System.out.println(empId);

        try {

            Connection con= connexion.connecterDB();
            PreparedStatement statment=con.prepareCall("{call `addFonction`(?,?)}");
            statment.setInt(1, idFunction);
            statment.setString(2,cinEmp);

            statment.execute();
            con.close();
            this.dispose();
            Employe_form e=new Employe_form();
            e.show();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
        getAllEmp();

    }//GEN-LAST:event_submitActionPerformed

